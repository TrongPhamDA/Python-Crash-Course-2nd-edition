#Sorting
print("\nSorting")
cars = ['bmw', 'audi', 'toyota', 'subaru']
print(cars)
cars.sort()
print(cars)

#Reverse sorting
print("\nReverse sorting")
cars.sort(reverse=True)
print(cars)

#Sort tạm thời
print("\n Sorted()")
cars = ['bmw', 'audi', 'toyota', 'subaru']
print("Original list:")
print(cars)

print("\nSorted list:")
print(sorted(cars))

print("\nCome back to Original list:")
print(cars)

#Reverse list
#Đảo ngược toàn bộ list
print("\nReverse list")
print(cars)
cars.reverse()
print(cars)

#Length of List
print("\nLength of List")
cars.reverse()
print(cars)
print(len(cars))

